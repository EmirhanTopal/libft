#include "libft.h"

char	*ft_strmapi(char const *s, char (*f) (unsigned int, char))
{
	unsigned int	i;
	char			*a;

	if (!s || !f)
		return (NULL);
	i = 0;
	a = malloc (sizeof(char) * (ft_strlen(s) + 1));
	if (!a)
		return (NULL);
	while (s[i] != '\0')
	{
		a[i] = f(i, s[i]);
		i++;
	}
	a[i] = '\0';
	return (a);
}
